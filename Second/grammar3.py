# ################# 类型转换 #################

# type查看变量的数据类型
name = 'rocker'
age = 28
weight = 64.5
courses = ['english', 'computer', 'math']
user = {'name': 'rocker', 'age': 28}
print(type(name))
print(type(age))
print(type(weight))
print(type(courses))
print(type(user))

# 类型转换
int_weight = int(weight)
print(int_weight)
print(type(int_weight))
str_age = str(age)
print(str_age)
print(type(str_age))

print(age+weight)
